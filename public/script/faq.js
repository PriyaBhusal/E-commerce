function showMessage(message1,message2,message3,message4,message5)
    {
        const message =  document.getElementById(message1,message2,message3,message4,message5)
        {
            if (message.style.display==="none")
            {
                message.style.display = "block"
            }
            else
            {
                message.style.display="none"
            }
        }
    }